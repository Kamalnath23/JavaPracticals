package com.Java.Basics.T3Basics;

public class FloatingLiterals {
    public static void main(String[] args) {
        float f1= 3.142f;
        System.out.println(f1);

        double d1= 3.142;
        System.out.println(d1);

        double d2= 5.63842e3;
        System.out.println(d2);

        float f2= 7.63842e4f;
        System.out.println(f2);

    }
}
